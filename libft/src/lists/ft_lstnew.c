#include "../../inc/libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = (t_list *)malloc(sizeof(t_list));
	if (!lst)
		return (NULL);
	else
		lst->content = content;
	lst->next = NULL;
	return (lst);
}
